# 7. Verificação de turno
# Peça ao usuário para digitar M para manhã ou qualquer outra tecla para tarde. Se for M, exiba Bom dia!, senão exiba Boa tarde!.


m = str(input("Informe uma letra para representar o seu dia: "))


if m == 'M':
        print ("Bom dia!!")
else:
        print ("Boa tarde!!")
