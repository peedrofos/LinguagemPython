# 3. Autorização de entrada em um evento
# Se a pessoa tiver um convite válido, exiba Entrada permitida, caso contrário, exiba Entrada negada.


convite = str(input("Você possui um convite válido: Responda com SIM ou NÃO: "))


if convite == 'SIM':
        print ("Entrada PERMITIDA")
else:
        print ("Entrada NEGADA")
