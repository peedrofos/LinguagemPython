# 8. Entre com dois números e exiba as comparações:

# a) Se o primeiro número é maior que o segundo


num1 = 2
num2 = 3
if num1 > num2:
      print("O primeiro número é maior que o segundo")
else:
      print("O primeiro número é menor que o segundo")


# b) Se os dois números são iguais


num1 = 2
num2 = 3
if num1 == num2:
      print("Os numeros são iguais")
else:
      print("Os numeros não são iguais")


# c) Se o primeiro número é menor ou igual ao segundo


num1 = 2
num2 = 3
if num1 <= num2:
      print("O primeiro número é menor ou igual ao segundo número")
else:
      print("O primeiro número é maior que o segundo número")
